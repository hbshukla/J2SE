package day5_Looping;

public class DoWhileLoop {

	public static void main(String[] args) 
	{
		int i=1;
		do
		{
			System.out.println(i);
			i++;
		} while(i<=10);

	System.out.println("//Print 10 to 1");	
	int a =10;
	do
	{
		System.out.println(a);
		a--;
	}while(a>=1);
	}

}
