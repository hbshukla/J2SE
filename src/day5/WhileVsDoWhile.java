package day5;

public class WhileVsDoWhile {

	public static void main(String[] args) 
	{
	
		int i =1;
		while(i<=5)
		{
			System.out.println(i);
			i++;
		}
		
		System.out.println("//Do While Loop");
	
		int a=1;
		do
		{
			
			System.out.println(a);
			a++;
		}while(a<=5);
	}

}
