package day3;

public class AssignmentOperators {

	public static void main(String[] args) {
		// Assignment Operator = += -= *= /= %=
		//ex 1
		int a = 10;
		
		a+=5; //a=a+5;
		System.out.println(a);
		
		int b=10;
		b=b-5;
		System.out.println(b);

		//ex 2
		int i = 10;
		i*=2; //i=i*2;
		System.out.println(i);
		
		
		//ex 3
		int c=10;
		c/=2;//c=c/2;
		System.out.println(c);
	}

}
